<b>Opis Gry:<br></b>
Aleara MMPRPG to jedna z najlepszych polskich gier MMORPG w klimacie fantasy. Surowa fabuła gry jest zawarta w filmiku na stronie głównej. Gracze sami rozbudowują fabułę pisząc sesje w Bramie Przygód. Każdy gracz ma swoje ID (Numer Identyfikacyjny). To liczba przy nicku. Grać można na trzy sposoby. 1) Fabularnie - Pisząc sesje klimatyczne. 2) Mechanicznie - dążąc do jak najlepszego wyniku w rankingu. 3) Pomocniczo - jako rzemieślnicy, wykonując przedmioty. W krainie są różne rangi. Gracza z rangą inną niż Mieszkaniec łatwo można rozpoznać - na liście online która znajduje się w prawej części gry obok nicku widnieje ikonka rangi. Każda osoba z rangą ma swoje prawa i obowiązki. Expić (zdobywać doświadczenie i wbijać lvle) można na wiele sposobów. Jak najlepiej? To zależne od tego jaką wybierzemy klasę postaci. Najprostszym sposobem dla wszystkich klas poza rzemieślnikiem jest arena walk. Tocząc walki na arenie zyskujemy dużą ilość doświadczenia. Exp można też zdobywać prowadząc swoją farmę, chodząc do burdelu, prowadząc swoją strażnicę, walcząc PvP z innymi graczami, wykonując przedmioty (rzemieślnicy - łuki, strzały, tarcze, broń biała, nagolenniki, pierścienie itp.), podróżując po lokacjach przeznaczonych do wędrówek. Sposobów jest wiele. Od tego ile możemy zrobić w grze w ciągu godziny decyduje nasza energia. Jest wyświetlana w panelu głównym gry (pod logiem). Np: Energia: 1633.31/2630.10 (+45). To co widzimy pokazuje nam, że mamy 1633 energii. Każda nasza czynność w grze pobiera energię (nie każdy ruch tylko np. walka na arenie itp). Liczba (+45) oznacza, że tyle dostajemy nowej energi na reset (reset - jest co równą godzinę – inaczej tura.). Aby zwiększyć dostawaną energię na reset - należy wbijać lvle albo po prostu zakupić KP (Konto Premium). Liczba 2630.10 w moim przykładzie wskazuje ile możemy mieć maksymalnej energii. Gdy posiadamy już tyle energii jaki mamy limit - oznacza to, że limit wyczerpaliśmy i mimo resetów energia już nam nie rośnie i musimy zużyć naszą energię. Dlaczego tak mało energii na reset? Ja tutaj na przykładnie dostaję 45 energii na reset. Niby mało, lecz można wybudować sobie domek w którym co godzinę możemy odpoczywać i dostajemy sporą ilość energii. To samo tyczy się zagrody koni. Tak na prawdę wiele zależy od aktywności gracza. Jeśli myślisz, że zaczynając teraz grać, a inni grają już dużo dłużej - nie masz szans wbić na najlepsze miejsca w rankingu to jesteś w błędzie. Wystarczy, że co reset (godzinę) robisz odpoczynki w domu i zagrodzie koni, zbierasz energię i ją przeznaczasz na expienie. Na początek gdy rozpoczynamy grę proponuję wejść w opcje konta które znajdują się w lewym menu gry i ustawić sobie avatar, przyszykować profil, ustawić klimatyczny opis, oraz wejść w opcje dodatkowe i edytory tekstu itp. Jeśli komuś przeszkadza rozwijane menu po lewej - może je zmienić w opcjach dodatkowych. <br><br>

<b>Jak zacząć grać?</b>
To bardzo proste. na początku jak wiadomo rejestrujemy się poprzez stronę główną. Gdy mamy już swoje konto - logujemy się do gry w prawej części strony głównej. Co teraz? Czas stworzyć postać. W tym celu wchodzimy do Kreatora Postaci. Znajduje on się w Statystykach(Lewe menu gry w dziale POSTAĆ. Gdy już tam znajdujemy się to klikamy "Rozpocznij tworzenie swojej postaci". Kolejnym krokiem jaki musimy wykonać to wybranie naszej płci. Zaznaczamy odpowiednią kropkę i klikamy wybierz. Później czeka nas wybór rasy. Mamy 16 różnych ras. Każda charakteryzuje się swoją mocą. Weźmy przykład. Rasa: Demon . Statystyki:<br>
<b>Demon otrzymuje za 1 AP:<br></b>
+2.2 Siły<br>
+3.7 Zręczności<br>
+3.3 Szybkości<br>
+2.5 Wytrzymałości<br>
+2.9 Inteligencji<br>
+2.3 Siły Woli<br>
AP - Astralne Punkty. Za każdy wbity lvl otrzymujemy takie punkty które wymieniamy na statystyki. Jak na przykładzie +2.2 Siły - bardzo mało. Z tego widzimy już, że postać nie jest dobra do walki wręcz, ponieważ siła u fizoli (osoby walczącej wręcz) odpowiada za DMG (obrażenia). +3.7 Zręczności +3.3 Szybkości - za to widzimy dobre parametry zręczności i szybkości co oznacza, że postać będzie świetna gdy będziemy walczyli dystansowo (łukiem). +2.9 Inteligencji +2.3 Siły Woli - ukazuje nam to, iż demon jest kiepskim magiem. Jaki wniosek? Demon najlepszy jest do walk dystansowych. To jest tylko przykład omówienia jednej z 16 ras.
Gry mamy już wybraną rasę postaci czas wybrać klasę. Klas nie będę wam opisywał, ponieważ wszystko jest już znakomicie opisane. Wypieramy klasę postaci. Gdy to uczynimy kreator prosi nas o wybranie wyznania. Do tego nie musimy przywiązywać dużej wagi, ponieważ wyznanie możemy zmienić w każdym momencie gry. Kolejny krok to wybranie miejsca zamieszkania - obojętnie co wciśniemy. Zawsze można podróżować między miasteczkami. Po stworzeniu postaci pojawiamy się w mieście które wybraliśmy. Kolejnym krokiem jaki powinniście zrobić to w lewym menu gry(menu nawigacji) kliknąć w POSTAĆ i wybrać statystyki. Jak widzimy wyświetla nam AP:10 (Użyj). Co oznacza, że mamy do rozdania 10 punktów astralnych, więc klikamy użyj. Jak rozdajemy punkty? To zależne od wybranej rasy i klasy oraz oczywiście uznania.
<br><b>Siła</b> - u fizola odpowiada głównie za DMG u dystansowca w mniejszym stopniu za DMG.<br>
<b>Zręczność</b> - U fizola odpowiada za celność ataków i uniki. U dystansowca odpowiada za główne DMG, celność ataków i uniki.<br>
<b>Szybkości</b> - To wiadomo, Od tego zależy ilość ataków jakie zadamy zanim przeciwnik zada cios<br>
<b>Wytrzymałość</b> - Odpowiada za HP postaci.<br>
<b>Inteligencja</b> - DMG u maga i siłę umagiczniania przedmiotów.<br>
<b>Siła Woli</b> - Obrona i mana u maga.<br>
Gdy wydamy AP to czas zakupić EQ (ekwipunek) w mieście. Odnośnik do miasta znajduje się w menu po lewej w dziale LOKACJA. EQ kupujemy od płatnerza, zbrojmistrza lub innych graczy. Możemy też EQ poszukać w sklepach graczy bądź na rynku. Teraz robicie co chcecie :)<br><br>

